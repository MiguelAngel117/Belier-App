    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            int column = jTable1.getSelectedColumn();
            int value = (int) jTable1.getValueAt(row, 0); 
            System.out.println("Valor de la celda: " + value);
            
            PurchaseCRUD pcrud = new PurchaseCRUD();
            
            Purchase purchase = null;
            try {
                purchase = pcrud.getPurchase(value);
            } catch (SQLException ex) {
                Logger.getLogger(JPanelReportPurchases.class.getName()).log(Level.SEVERE, null, ex);
            }
            Garment garment = null;
            try {
                garment = new GarmentCRUD().getGarment(purchase.getGarment());
            } catch (SQLException ex) {
                Logger.getLogger(JPanelReportPurchases.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                if(new GarmentCRUD().isExistGarmentUpdate(purchase.getGarment())){
                    JPanelViewPurchase jpua = new JPanelViewPurchase(jFrameMain,jpanelMain);
                    jpanelMain.initPanel(jpua);
                    
                    jpua.getjTextField6().setText(String.valueOf(purchase.getId()));
                    jpua.getjTextField2().setText(purchase.getGarment());
                    jpua.getjTextField8().setText(garment.getName());
                    jpua.getjTextField5().setText(garment.getSex());
                    jpua.getjTextField7().setText(garment.getNameType());
                    
                    jpua.getjTextField4().setText(purchase.getUnitPrice());
                    jpua.getjTextField3().setText(String.valueOf(purchase.getQuantity()));
                    jpua.getjTextField1().setText(purchase.getTotal());
                    
                }
                else{
                    new Service().initNotification("El producto de la compra ya no se encuentra registrado en el sistema.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(JPanelReportPurchases.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

