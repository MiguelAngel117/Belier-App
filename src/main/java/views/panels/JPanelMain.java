    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        File file = new File("src/main/resources/ManualSI.pdf");
        if (Desktop.isDesktopSupported()) {
            Desktop desktop = Desktop.getDesktop();
            if (file.exists()) {
                try {
                    desktop.open(file);
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked
    
    public void setBackgroundClicked(JButton jButtonOn){        
        try{
            switch (jButtonOn.getActionCommand()) {
                case "   Principal" -> {
                    jButtonMain.setBackground(new Color(176,75,0));
                    jButtonArticles.setBackground(new Color(255,102,0));
                    jButtonBuy.setBackground(new Color(255,102,0));
                    jButtonReport.setBackground(new Color(255,102,0));
                    jButtonSale.setBackground(new Color(255,102,0));      
                    revalidate();
                    repaint();
                }
                case "   Ventas" -> {
                    jButtonSale.setBackground(new Color(176,75,0));
                    jButtonMain.setBackground(new Color(255,102,0));
                    jButtonArticles.setBackground(new Color(255,102,0));
                    jButtonBuy.setBackground(new Color(255,102,0));
                    jButtonReport.setBackground(new Color(255,102,0));
                    revalidate();
                    repaint();
                }
                case "   Productos" -> {
                    jButtonArticles.setBackground(new Color(176,75,0));
                    jButtonMain.setBackground(new Color(255,102,0));
                    jButtonBuy.setBackground(new Color(255,102,0));
                    jButtonReport.setBackground(new Color(255,102,0));
                    jButtonSale.setBackground(new Color(255,102,0));      
                    revalidate();
                    repaint();
                }
                case "   Compras" -> {
                    jButtonBuy.setBackground(new Color(176,75,0));
                    jButtonMain.setBackground(new Color(255,102,0));
                    jButtonArticles.setBackground(new Color(255,102,0));
                    jButtonReport.setBackground(new Color(255,102,0));
                    jButtonSale.setBackground(new Color(255,102,0));      
                    revalidate();
                    repaint();
                }
                case "   Reportes" -> {
                    jButtonReport.setBackground(new Color(176,75,0));
                    jButtonMain.setBackground(new Color(255,102,0));
                    jButtonArticles.setBackground(new Color(255,102,0));
                    jButtonBuy.setBackground(new Color(255,102,0));
                    jButtonSale.setBackground(new Color(255,102,0));      
                    revalidate();
                    repaint();
                }
            }              
        } catch(Exception e){
            
        }
    }

